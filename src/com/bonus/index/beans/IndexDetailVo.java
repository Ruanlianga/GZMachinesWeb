package com.bonus.index.beans;

import java.io.Serializable;

/**
 * @author xj
 * 机具日历详情-vo
 * 
 */
public class IndexDetailVo implements Serializable{
	
	private static final long serialVersionUID = 1L;

	
	/**
	 * 工程ID
	 */
	private String proId;
	
	
	/**
	 * 工程名称
	 */
	private String proName;
	
	
	/**
	 * 领料出库数量
	 */
	private int leaseNum;
	
	/**
	 * 退料接收
	 */
	private int backNum;
	
	/**
	 * 维修检验
	 */
	private int checkNum;

	/**
	 * 机具报废
	 */
	private int scrapNum;
	
	/**
	 * 新购入库
	 */
	private int newNum;
	
	/**
	 * 库存盘点
	 */
	private int bdNum;
	
	/**
	 * 修试入库
	 */
	private int inputNum;
	

	public String getProId() {
		return proId;
	}

	public void setProId(String proId) {
		this.proId = proId;
	}

	public String getProName() {
		return proName;
	}

	public void setProName(String proName) {
		this.proName = proName;
	}

	public int getLeaseNum() {
		return leaseNum;
	}

	public void setLeaseNum(int leaseNum) {
		this.leaseNum = leaseNum;
	}

	public int getBackNum() {
		return backNum;
	}

	public void setBackNum(int backNum) {
		this.backNum = backNum;
	}

	public int getCheckNum() {
		return checkNum;
	}

	public void setCheckNum(int checkNum) {
		this.checkNum = checkNum;
	}

	public int getScrapNum() {
		return scrapNum;
	}

	public void setScrapNum(int scrapNum) {
		this.scrapNum = scrapNum;
	}

	public int getNewNum() {
		return newNum;
	}

	public void setNewNum(int newNum) {
		this.newNum = newNum;
	}

	public int getBdNum() {
		return bdNum;
	}

	public void setBdNum(int bdNum) {
		this.bdNum = bdNum;
	}

	public int getInputNum() {
		return inputNum;
	}

	public void setInputNum(int inputNum) {
		this.inputNum = inputNum;
	}
	

	@Override
	public String toString() {
		return "IndexDetailVo [proId=" + proId + ", proName=" + proName + ", leaseNum=" + leaseNum + ", backNum=" + backNum
				+ ", checkNum=" + checkNum + ", scrapNum=" + scrapNum + ", newNum=" + newNum + ", bdNum=" + bdNum
				+ ", inputNum=" + inputNum + "]";
	}
	
	

}
