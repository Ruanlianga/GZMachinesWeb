package com.bonus.index.beans;

import java.util.List;

public class PartThreeBean {
	
	
	private String id;
	
	private int rank;
	
	private String projectName;
	
	private String useCount;
	
	private String returnCount;
	
	private String diffCount;
	
	private List<PartThreeBean> rankList;
	
	

	public List<PartThreeBean> getRankList() {
		return rankList;
	}

	public void setRankList(List<PartThreeBean> rankList) {
		this.rankList = rankList;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getUseCount() {
		return useCount;
	}

	public void setUseCount(String useCount) {
		this.useCount = useCount;
	}

	public String getReturnCount() {
		return returnCount;
	}

	public void setReturnCount(String returnCount) {
		this.returnCount = returnCount;
	}

	public String getDiffCount() {
		return diffCount;
	}

	public void setDiffCount(String diffCount) {
		this.diffCount = diffCount;
	}

	
	
}
