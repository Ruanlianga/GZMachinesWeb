package com.bonus.index.beans;

public class PartFiveBean {
	
	
	private String id;
	
	private String newNum;
	
	private String leaseNum;
	
	private String backNum;
	
	private String scrapNum;

	private String bdNum;
	
	private String checkNum;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNewNum() {
		return newNum;
	}

	public void setNewNum(String newNum) {
		this.newNum = newNum;
	}

	public String getLeaseNum() {
		return leaseNum;
	}

	public void setLeaseNum(String leaseNum) {
		this.leaseNum = leaseNum;
	}

	public String getBackNum() {
		return backNum;
	}

	public void setBackNum(String backNum) {
		this.backNum = backNum;
	}

	public String getScrapNum() {
		return scrapNum;
	}

	public void setScrapNum(String scrapNum) {
		this.scrapNum = scrapNum;
	}

	public String getBdNum() {
		return bdNum;
	}

	public void setBdNum(String bdNum) {
		this.bdNum = bdNum;
	}

	public String getCheckNum() {
		return checkNum;
	}

	public void setCheckNum(String checkNum) {
		this.checkNum = checkNum;
	}
	
	
	
}
